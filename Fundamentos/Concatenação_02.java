public class Java_Treino {
    public static void main(String[] args) {

        // Usando o String.format = O método format é ideal para criar strings formatadas.

        String nome = "Eduardo";
        int idade = 16;
        String mensagem = String.format("Meu nome é %s e tenho %d anos.", nome, idade);
        System.out.println(mensagem);
    }
}
