import java.util.Scanner;

public class Main {
    public static void main(String[] args) {

        // Pergunta um número para o úsuario
        Scanner PrimeiroNumber = new Scanner(System.in);
        System.out.print("Digite um número: ");
        int primerNumber = PrimeiroNumber.nextInt();
        System.out.println(primerNumber);

        // Pergunta outro número ao úsuario
        Scanner SegundoNumber = new Scanner(System.in);
        System.out.print("Digite outro Número: ");
        int segundNumber = SegundoNumber.nextInt();
        System.out.println(segundNumber);

        // SOMA para os números fornecidos do úsuario
        int soma = primerNumber + segundNumber;
        System.out.print("A soma é igual a: " + soma);
    }
}
